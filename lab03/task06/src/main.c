#include <stdio.h>

int main() {

    //Дiапазон
    int x1 = 30;
    int x2 = 33;

    //Сума
    int sum = 0;


    //Зробимо цикл, де змінна sum буде отримувати значення y, яка буде збільшуватися на одиницю, поки виконується умова
    for(int y = x1; y <= x2; y ++) {
        sum += y;  //sum - сума дiапазону
    }
    

    return 0;

}
